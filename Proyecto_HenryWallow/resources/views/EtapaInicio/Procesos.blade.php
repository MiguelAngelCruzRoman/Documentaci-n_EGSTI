<div class="container">
  <h1 class="mt-5">Procesos de Inventario del instituto Henry Wallon</h1>
  <p>Los procesos de inventario en una escuela son fundamentales para gestionar los activos físicos y recursos utilizados en las instalaciones educativas. Aquí se detallan los pasos generales para realizar estos procesos:</p>
  
  <ol>
    <li>Identificación de activos.</li>
    <li>Asignación de responsabilidades.</li>
    <li>Creación de un sistema de seguimiento.</li>
    <li>Inventario inicial.</li>
    <li>Actualización periódica.</li>
    <li>Identificación de activos perdidos o dañados.</li>
  </ol>
</div>
<div>
    <h3>Identificación de activos</h3>
    <p>
    Primero, se debe identificar y catalogar todos los activos que necesitan ser incluidos en el inventario. 
    Esto puede incluir equipos de laboratorio, computadoras, mobiliario, libros de texto, suministros de oficina, material didáctico, etc.
    </p>
</div>

<div>
<h3>Asignación de responsabilidades</h3>
    <p>
    Se designa a un encargado o equipo responsable del inventario. 
    Este personal será responsable de llevar un registro preciso de los activos y realizar conteos periódicos.
    </p>
</div>

<div>
<h3>Creación de un sistema de seguimiento</h3>
    <p>
    Se establece un sistema de seguimiento que puede ser manual o electrónico, 
    dependiendo de los recursos disponibles y la escala del inventario.
    </p>
</div>

<div>
<h3>Inventario inicial</h3>
    <p>
    Se realiza un inventario inicial para registrar todos los activos existentes en la escuela. 
    Este proceso puede ser llevado a cabo por el personal designado para esta tarea.
    </p>
</div>

<div>
<h3>Actualización periódica</h3>
    <p>
    Se establece un programa regular de actualización del inventario. 
    Esto puede incluir conteos físicos periódicos de los activos, 
    así como la actualización de registros en el sistema de seguimiento.
    </p>
</div>


<div>
<h3>Identificación de activos perdidos o dañados</h3>
    <p>
    Durante los conteos periódicos, se identifican los activos que están perdidos,
     dañados o fuera de servicio. Se registra esta información y se toman las medidas necesarias, 
     como la reparación, el reemplazo o la eliminación.
    </p>
</div>

